#!/usr/bin/env python3
"""U3 180°回転＋ネット再割当の新規ルート一括検証。"""
import check_route as cr

items = cr.load(cr.PCB)

RIP_SEGS = {
    # +3.3V 旧pad2/pad12系の枝（給電先消滅）
    (11.8,23.0,13.25,23.0),(13.25,23.0,13.25,23.738),(13.25,23.738,13.3,23.738),
    (13.3,23.738,13.3,24.25),(13.3,24.25,12.7375,24.25),
    # SCL旧pad3タップ
    (13.2,26.5,13.2,24.75),(13.2,24.75,12.738,24.75),
    # SDA東ループ全部
    (9.15,25.95,9.15,29.55),(9.15,29.55,13.3,29.55),(13.3,29.55,13.3,29.15),
    (13.3,29.15,16.8,29.15),(16.8,29.15,16.8,29.225),(16.8,29.225,14.875,29.225),
    (14.875,29.225,14.875,23.4),(14.875,23.4,15.5,23.4),(15.5,23.4,13.65,23.4),
    (13.65,23.4,13.75,23.4),(13.75,23.4,13.75,23.7375),
    (13.35,21.0,13.35,21.6),(13.35,21.6,15.5,21.6),(15.5,21.6,15.5,23.4),
    # ACCEL旧
    (14.2625,24.25,14.2625,25.25),(14.2625,25.25,14.2625,29.85),
    (14.2625,29.85,10.85,29.85),(10.85,29.85,10.85,25.95),
    # GND旧スタブ（pin4/pin12位置になるもの）
    (14.2625,23.75,14.95,23.75),(13.75,25.2625,13.75,25.9),
}
RIP_VIAS = {(11.8,23.0),(9.15,29.55),(16.8,29.225),(15.5,23.4),(14.2625,25.25)}

def ripped(it):
    if it[0]=='seg':
        return (it[3],it[4],it[5],it[6]) in RIP_SEGS or (it[5],it[6],it[3],it[4]) in RIP_SEGS
    if it[0]=='via':
        return (it[3],it[4]) in RIP_VIAS
    return False

# U3 180°回転後のパッド位置と新ネット（global）
ROT = {  # pad: (x, y, net)
    '1': (14.2625,25.25,'I2C_SCL'), '2': (14.2625,24.75,'+3.3V'), '3': (14.2625,24.25,'GND'),
    '4': (14.2625,23.75,'I2C_SDA'), '5': (13.75,23.7375,''), '6': (13.25,23.7375,'GND'),
    '7': (12.7375,23.75,'GND'), '8': (12.7375,24.25,'GND'), '9': (12.7375,24.75,'+3.3V'),
    '10': (12.7375,25.25,'+3.3V'), '11': (13.25,25.2625,''), '12': (13.75,25.2625,'ACCEL_INT1'),
}
fixed=[]
for it in items:
    if ripped(it): continue
    if it[0]=='pad' and it[7].startswith('U3.'):
        p = it[7][3:]
        x,y,net = ROT[p]
        it = ('pad', net, it[2], x, y, it[5], it[6], it[7])
    fixed.append(it)
print(f'検証対象 {len(fixed)}（rip {len(items)-len(fixed)}除外）')

S,V='seg','via'
c=[]
# SDAメイン: via(9.15,25.95)→B北上→y=19.07→蛇行→y=19.45東進→via(15.2,20.5)→F東回り→pin4
# R6.2プルアップはB縦x=13.35からのT分岐→via(13.35,21.0)→F→R6.2
c+=[(S,'I2C_SDA','B.Cu',9.15,25.95,9.15,22.0,0.15),(S,'I2C_SDA','B.Cu',9.15,22.0,9.3,22.0,0.15),
    (S,'I2C_SDA','B.Cu',9.3,22.0,9.3,18.45,0.15),(S,'I2C_SDA','B.Cu',9.3,18.45,12.28,18.45,0.15),
    (S,'I2C_SDA','B.Cu',12.28,18.45,12.28,19.45,0.15),(S,'I2C_SDA','B.Cu',12.28,19.45,15.35,19.45,0.15),
    (S,'I2C_SDA','B.Cu',13.35,19.45,13.35,21.0,0.15),
    (S,'I2C_SDA','B.Cu',15.35,19.45,15.35,20.1,0.15),(S,'I2C_SDA','B.Cu',15.35,20.1,15.2,20.1,0.15),
    (S,'I2C_SDA','B.Cu',15.2,20.1,15.2,20.5,0.15),
    (V,'I2C_SDA',15.2,20.5),
    (S,'I2C_SDA','F.Cu',15.2,20.5,15.2,21.3,0.15),(S,'I2C_SDA','F.Cu',15.2,21.3,15.5,21.3,0.15),
    (S,'I2C_SDA','F.Cu',15.5,21.3,15.5,23.3,0.15),(S,'I2C_SDA','F.Cu',15.5,23.3,14.2625,23.3,0.15),
    (S,'I2C_SDA','F.Cu',14.2625,23.3,14.2625,23.75,0.15)]
# SCL: 既存東枝(15.0,25.2)→pin1
c+=[(S,'I2C_SCL','F.Cu',15.0,25.2,14.2625,25.2,0.2),(S,'I2C_SCL','F.Cu',14.2625,25.2,14.2625,25.25,0.2)]
# +3.3V: pin10→pin9列 + pin9→pin2橋
c+=[(S,'+3.3V','F.Cu',12.7375,25.25,12.7375,24.75,0.2),
    (S,'+3.3V','F.Cu',12.7375,24.75,14.2625,24.75,0.2)]
# GND: pin7→pin8列 + pin8→pin3橋 + pin7→pin6
c+=[(S,'GND','F.Cu',12.7375,23.75,12.7375,24.25,0.2),
    (S,'GND','F.Cu',12.7375,24.25,14.2625,24.25,0.2),
    (S,'GND','F.Cu',12.7375,23.75,13.25,23.7375,0.15)]
# ACCEL: via(10.85,25.95)→B南→y=27.0→x=13.75→via(13.75,25.95)→F→pin12
c+=[(S,'ACCEL_INT1','B.Cu',10.85,25.95,10.85,27.0,0.2),
    (S,'ACCEL_INT1','B.Cu',10.85,27.0,13.75,27.0,0.2),
    (S,'ACCEL_INT1','B.Cu',13.75,27.0,13.75,25.95,0.2),
    (V,'ACCEL_INT1',13.75,25.95),
    (S,'ACCEL_INT1','F.Cu',13.75,25.95,13.75,25.2625,0.2)]

bad = cr.check(c, fixed, 'rot')
print(f'\n違反 {len(bad)} 件:')
for b in sorted(set(bad)): print(' ', b)
